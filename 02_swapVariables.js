// swap two variables using third variable

var name1 = "Sweety";
var name2 = "Cutie";
var temp;

console.log("Before swapping:","name 1:-",name1,"name 2:-",name2);

temp = name1;
name1 = name2;
name2 = temp;

console.log("After swapping:","name 1:-",name1,"name 2:-",name2);

